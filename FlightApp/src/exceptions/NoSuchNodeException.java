package exceptions;

/**
 * An exception for an invalid Node call.
 * @author Faisal Usmani
 */
@SuppressWarnings("serial")
public class NoSuchNodeException extends Exception {

	/**
	 * Creates a NoSuchNodeException
	 */
	public NoSuchNodeException() {
		
	}

	/**
	 * Creates a NoSuchNodeException with the given message
	 * @param message the massage to be displayed.
	 */
	public NoSuchNodeException(String message) {
		super(message);
		
	}

}
